// m to km
module.exports.convertMToKM = function (value){
    return value/1000;
}
// km to m
module.exports.convertKMToM =function (value){
    return value*1000;
}
// g to kg
module.exports.convertGToKG = function (value){
    return value/1000;
}
// kg to g
module.exports.convertKGToG = function (value){
    return value*1000;
}